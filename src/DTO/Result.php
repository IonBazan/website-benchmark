<?php

declare(strict_types=1);

namespace App\DTO;

abstract class Result
{
    /**
     * @var mixed
     */
    protected $value;

    /**
     * @param mixed $value
     */
    public function __construct($value)
    {
        $this->value = $value;
    }

    /**
     * @return mixed
     */
    public function getValue()
    {
        return $this->value;
    }

    abstract public function getUnit(): string;
}
